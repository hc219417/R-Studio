#data types

  #a data matrix is a collection of individuals (rows) and measurements
  #or descriptions of these individuals called variables (columns)

  #rows are individuals (cases, observations, etc.)
  #columns are variables

#types of variables

  #numerical (or quantitative)

    #continuous (any number provided a precise enough measuring tool)

      #example: HDL, or cholesterol (mg/dL) - 40.5, 55.3, 60.7, 45.6, 65.4

    #discrete (integer values)

      #example: age (years) - 22, 25, 18, 13, 45, 60, 41, 13, 60

  #categorical (or qualitative)

    #nominal (no real order)

      #example: genotype - AA, Aa, aa, AA, AA, Aa, Aa, AA, Aa, Aa

    #ordinal (defined order)

      #example: letter grade - A, A, B, A, C, D, F, A, B, C, A, A

#where does data come from?

  #population (all individuals of interest)

  #sample data (subset of individuals)

